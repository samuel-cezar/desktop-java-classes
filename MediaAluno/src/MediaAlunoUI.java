    private double parseNota(String texto) {
        if (texto == null) {
            throw new IllegalArgumentException("Preencha todas as notas.");
        }
        String normalized = texto.trim().replace(',', '.');
        if (normalized.isEmpty()) {
            throw new IllegalArgumentException("Preencha todas as notas.");
        }
        return Double.parseDouble(normalized);
    }

    private void validarIntervalo(double... notas) {
        for (double n : notas) {
            if (n < 0.0 || n > 10.0) {
                throw new IllegalArgumentException("As notas devem estar entre 0 e 10.");
            }
        }
    }

