    private void handleInputNumero(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_handleInputNumero
        char c = evt.getKeyChar();
        if (!Character.isDigit(c) && c != '.' && c != ',' && c != java.awt.event.KeyEvent.VK_BACK_SPACE) {
            evt.consume();
        }
    }//GEN-LAST:event_handleInputNumero

