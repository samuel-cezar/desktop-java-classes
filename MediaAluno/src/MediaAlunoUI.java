    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        try {
            double n1 = parseNota(txtP1.getText());
            double n2 = parseNota(txtP2.getText());
            double n3 = parseNota(txtP3.getText());
            double n4 = parseNota(txtP4.getText());
            validarIntervalo(n1, n2, n3, n4);

            String modo = (String) cmbClasse.getSelectedItem();
            String saida;

            if ("Aluno".equals(modo)) {
                Aluno a = new Aluno();
                a.carregaNotas(n1, n2, n3, n4);
                // Usa a string formatada da própria classe
                saida = a.situacaoCompleta();
            } else {
                Aluno2 a2 = new Aluno2();
                a2.recebeNota1(n1);
                a2.recebeNota2(n2);
                a2.recebeNota3(n3);
                a2.recebeNota4(n4);
                // Em GUI, preferimos montar a string em vez de imprimir no console
                saida = String.format("Media: %.1f -> %s.", a2.obtemMedia(), a2.obtemSituacao());
            }

            txtResultado.setText(saida);
        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(
                    this, "Use números válidos (0–10). Ex.: 7.5 ou 7,5", "Entrada inválida",
                    javax.swing.JOptionPane.WARNING_MESSAGE
            );
        } catch (IllegalArgumentException ex) {
            javax.swing.JOptionPane.showMessageDialog(
                    this, ex.getMessage(), "Entrada inválida",
                    javax.swing.JOptionPane.WARNING_MESSAGE
            );
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

