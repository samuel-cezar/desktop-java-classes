    private void redirectSystemOutToTextArea(javax.swing.JTextArea targetArea) {
        java.io.OutputStream out = new java.io.OutputStream() {
            @Override
            public void write(int b) throws java.io.IOException {
                appendText(String.valueOf((char) b));
            }

            @Override
            public void write(byte[] b, int off, int len) throws java.io.IOException {
                String text = new String(b, off, len, java.nio.charset.StandardCharsets.UTF_8);
                appendText(text);
            }

            private void appendText(String text) {
                javax.swing.SwingUtilities.invokeLater(() -> targetArea.append(text));
            }
        };
        System.setOut(new java.io.PrintStream(out, true, java.nio.charset.StandardCharsets.UTF_8));
    }

